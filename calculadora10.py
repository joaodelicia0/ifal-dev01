#solicitar os valores a serem calculados
x = float(input("qual o primeiro valor"))
y = float(input("qual o segundo valor"))
#realizar o cálculo

#exibir o resultado para o usuário
print(f"A soma de {x} + {y} é {x + y}")
print(f"A subtração de {x} - {y} é {x - y}")
print(f"A multiplicação de {x} * {y} é {x * y}")
print(f"A divisão de {x} / {y} é {round(x / y, 3)}")
print(f"O resto de {x} % {y} é {x % y}")


